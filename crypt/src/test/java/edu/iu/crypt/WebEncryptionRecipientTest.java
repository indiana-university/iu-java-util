package edu.iu.crypt;

import static org.mockito.Mockito.CALLS_REAL_METHODS;
import static org.mockito.Mockito.mock;
import static org.mockito.Mockito.verify;
import static org.mockito.Mockito.when;

import java.io.ByteArrayInputStream;

import org.junit.jupiter.api.Test;

@SuppressWarnings("javadoc")
public class WebEncryptionRecipientTest {

	@Test
	public void testEncrypt() {
		final var r = mock(WebEncryptionRecipient.Builder.class, CALLS_REAL_METHODS);
		final var b = mock(WebEncryption.Builder.class);
		when(r.then()).thenReturn(b);

		r.encrypt("");
		verify(b).encrypt("");

		final var a = new byte[0];
		r.encrypt(a);
		verify(b).encrypt(a);
		
		final var i = new ByteArrayInputStream(a);
		r.encrypt(i);
		verify(b).encrypt(i);
	}
	
}
